package operators.shortHandAssignmentOperators;

public class Exercise1 {
    public static void main(String[] args) {
        /*
        Create the variables
        Use shorthand assignments
        Print them out
         */

        int a = 8;
        int b = 3;



        System.out.println("a += b --> " +(a +=b));
        System.out.println("a -= b --> " +(a -=b));
        System.out.println("a *= b --> " +(a *=b));
        System.out.println("a /= b --> " +(a /=b));
        System.out.println("a %= b --> " +(a %=b));
    }
}


