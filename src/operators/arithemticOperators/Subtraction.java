package operators.arithemticOperators;

public class Subtraction {
    public static void main(String[] args) {

        int valueOfA = 3 - 1;
        int valueOfB = 2 - 1;

        System.out.println("The difference is: " + (valueOfA - valueOfB));

        int num1 = 5 - 2;
        int num2 = 6 - 4;
        int sum = num1 - num2;
        System.out.println("The numbers are: " + sum);
    }
}
